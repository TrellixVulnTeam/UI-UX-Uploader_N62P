// 변수 선언(문자 2개, 숫자 2개 선언)
var country = "대한민국";
var city = "수원";
var year = 2020;
var month = 12;

// 콘솔에 country + city
console.log(country + city);

// city + year
console.log(city + year);

// year + month
console.log(year + month);

// city + year + month
console.log(city + year + month);

// year + month + city
console.log(year + month + city);