// 변수 선언
var Num1 = 10;
var Num2 = 3;
var result;

// 사칙 연산 실행
// 덧셈
result = Num1 + Num2;
console.log(result);

// 뻴셈
result = Num1 - Num2;
console.log(result);

// 곱셈
result = Num1 * Num2;
console.log(result);

// 나눗셈
result = Num1 / Num2;
console.log(result);

// 나머지 값
result = Num1 % Num2;
console.log(result);