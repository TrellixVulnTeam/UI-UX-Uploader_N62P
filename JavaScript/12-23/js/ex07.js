// 변수 선언
var Num1 = 10;
var Num2 = 3;
var Num3 = 5;

// Num1 + Num2의 결과 삽입
Num1 += Num2;
console.log(Num1);

// Num1 - Num3의 결과를 Num1에 삽입
Num1 -= Num3;
console.log(Num1);

// Num1 x Num2의 결과 출력
Num1 *= Num2;
console.log(Num1);

// Num1 / Num2 의 결과 출력
Num1 /= Num2;
console.log(Num1);

// Num3 / Num2의 나머지 값 출력
Num3 %= Num2;
console.log(Num3);